partial class Program
{
    static double soma(double x, double y)
    {
        return x + y;
    }

    static double subtracao(double x , double y)
    {
        return x - y;
    }

    static double multiplicacao(double x , double y)
    {
        return x * y;
    }

    static double divisao(double x , double y)
    {
        return x / y;
    }
}
